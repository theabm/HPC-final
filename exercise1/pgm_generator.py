import struct

def write_binary_with_header(matrix, filename):
    # Define the custom header

    rows = len(matrix)
    cols = len(matrix[0])

    header = f"P5 {rows} {cols} 1\n"

    # Open the binary file for writing
    with open(filename, 'wb') as file:
        file.write(header.encode('utf-8'))

        # Iterate through the matrix and write each element as a single byte
        for row in matrix:
            for value in row:
                file.write(struct.pack('B', value))

def create_glider_gun_p5(filename):
    # Define the Gosper Glider Gun pattern as a list of lines
    glider_gun = [
        "........................O..........................",
        "......................O.O..........................",
        "............OO......OO............OO...............",
        "...........O...O....OO............OO...............",
        "OO........O.....O...OO.............................",
        "OO........O...O.OO....O.O..........................",
        "..........O.....O..........OO......................",
        "...........O...O...................................",
        "............OO....................................."
    ]

    # Determine the width and height of the pattern
    width = len(glider_gun[0])
    height = len(glider_gun)

    # Create the P5 PGM data (255 for 'O', 0 for '.')
    pgm_data = []
    for line in glider_gun:
        for char in line:
            if char == 'O':
                pgm_data.append(1)
            else:
                pgm_data.append(0)

    # Open the file and write the P5 PGM data
    with open(filename, 'wb') as pgm_file:
        pgm_file.write(f"P5 {width} {height} 1\n".encode())
        pgm_file.write(bytes(pgm_data))



